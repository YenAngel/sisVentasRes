    private void txtMontoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtMontoKeyTyped
       char c=evt.getKeyChar();         
        if(!Character.isDigit(c)) { 
            if (c != '.'){
                if((int)evt.getKeyChar() != 8){
                    getToolkit().beep();             
                    evt.consume();                         
                    JOptionPane.showMessageDialog(null, "Sólo debe ingresar números");
                }
            }else{
                boolean flag = false;
                for (int i = 0; i < txtMonto.getText().length();i++){
                    if(txtMonto.getText().charAt(i) == '.')
                        flag = true;
                }
                if(flag){
                    getToolkit().beep();             
                    evt.consume();}
            }    
        }else{
                boolean flag = false;
                for (int i = 0; i < txtMonto.getText().length();i++){
                    if(txtMonto.getText().charAt(i) == '.')
                        flag = true;
                }
                if(flag){
                    int longAfterpoint = txtMonto.getText().substring(txtMonto.getText().indexOf('.')+1,txtMonto.getText().length()).length();
                    if(longAfterpoint == 2){
                    getToolkit().beep();             
                        evt.consume();}
                }
                
        }
    }//GEN-LAST:event_txtMontoKeyTyped

